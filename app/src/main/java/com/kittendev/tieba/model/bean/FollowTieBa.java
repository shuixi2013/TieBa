package com.kittendev.tieba.model.bean;

public class FollowTieBa {
	
	private String link;
	private String logo;
	private String tile;
	private String level;
	private String inttro;
	
	public void setLink(String link) {
		this.link = link;
	}

	public String getLink() {
		return link;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getLogo() {
		return logo;
	}

	public void setTile(String tile) {
		this.tile = tile;
	}

	public String getTile() {
		return tile;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getLevel() {
		return level;
	}

	public void setInttro(String inttro) {
		this.inttro = inttro;
	}

	public String getInttro() {
		return inttro;
	}

}
